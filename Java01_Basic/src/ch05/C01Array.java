package ch05;

public class C01Array{
    public static void main(String[] args){

        //배열
        int a = 10;
        int b = 20;
        int c = 30;
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);

        int d[] = new int[3];
        d[0] = 10;
        d[1] = 20;
        d[2] = 30;

        System.out.println(d[0]);
        System.out.println(d[1]);
        System.out.println(d[2]);
    }
}