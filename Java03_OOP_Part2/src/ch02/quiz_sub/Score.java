package ch02.quiz_sub;

public class Score {

	private String sName;
	private String major;
	private int score;
	
	
	public String getName() {
		return sName;
	}
	public String getMajor() {
		return major;
	}
	public int getScore() {
		return score;
	}
	public void setName(String name) {
		this.sName = name;
	}
	public void setMajor(String major) {
		this.major = major;
	}
	public void setScore(int score) {
		this.score = score;
	}
	
	
}
