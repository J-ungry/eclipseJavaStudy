package ch02.quiz.case02;

public class Fruit {

	//fields
	private String fruitName;
	private int price;
	
	//getter,setter
	public String getFruitName() {
		return fruitName;
	}
	public int getPrice() {
		return price;
	}
	public void setFruitName(String fruitName) {
		this.fruitName = fruitName;
	}
	public void setPrice(int price) {
		this.price = price;
	}
	
	
}
